package com.deeparishi.javaapp.leetcode.graph.utls;

public class Pair {

    public int row;

    public int col;

    public int time;

    public Pair(int row, int col, int time) {
        this.row = row;
        this.col = col;
        this.time = time;
    }


    public Pair(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int first;

    public int second;

}
